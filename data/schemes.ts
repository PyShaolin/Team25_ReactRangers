// Helper function to get schemes by state
export const getSchemesByState = (state: string): Scheme[] => {
  return schemes.filter((scheme) => scheme.state === state || scheme.state === "All India")
}

// Helper function to search schemes
export const searchSchemes = (query: string): Scheme[] => {
  const lowercaseQuery = query.toLowerCase()
  return schemes.filter(
    (scheme) =>
      scheme.name.toLowerCase().includes(lowercaseQuery) ||
      scheme.summary.toLowerCase().includes(lowercaseQuery) ||
      scheme.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)) ||
      scheme.targetGroup.toLowerCase().includes(lowercaseQuery),
  )
}
